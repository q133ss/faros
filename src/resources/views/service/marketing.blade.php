                            Пассивное поле - это материалы, которые можно найти в свободном доступе, просто воспользовавшись поиском. Здесь довольно сложно спрогнозировать исходящий запрос, ведь мы точно не знаем, как именно мыслит потребитель. В этой части работы faros.media регулярно расширяет семантику запросов. Основная задача при работе с пассивным полем - это нивелирование негатива и наращивание позитива с целью сформировать выдачу поисковых систем по максимально широкой линейке запросов. Ядро запросов препаратов в поисковой системе мы формируем с помощью продуктов wordstat.yandex или topvisor. Конечная цель потребительского поведения -&nbsp;выбор в пользу нашего препарата и фарм.производителя.
